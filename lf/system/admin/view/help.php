<h2>A guide to LittleFoot</h2>
<p>LittleFoot is a content management system. 
I feel it is different from other CMS software because of how simple everything is. 
I have found that using an MVC structure for my apps makes everything so much easier.</p>


<h3>Dashboard</h3>
<p>The Dashboard is where you arranage your navigation menu. We can move items up and down as well as change the subdirectory it is in. If you set an item's position to 0, it becomes hidden from the navigation menu. This can be useful for things like a signup page where you would only want to link to it from the login application. Here is the dashboard with a nav item selected.</p>
